# escape sequence characters are special characters

# if we want to add a newline then we use 
# escape sequence   we use   \n

print("Wasiq is a good boy\nHe goes to Air university\nBYE BYE")


# if we want to add space as tab then we use \t

print("Hello\tWorld")

# if we want to print a double quote we will write it like \"

print("\"Wasiq\" is a good boy")

# if we want to add backslash we use \\

print("Yes\\NO")